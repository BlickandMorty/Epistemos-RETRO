// ── Type mappers (Tauri Page/Folder → store NotePage/NoteBook) ─────

function mapPageToNotePage(p: Page): NotePage {
  return {
    id: p.id,
    title: p.title,
    name: normalizePageName(p.title),
    isJournal: p.is_journal,
    journalDate: p.journal_date ?? undefined,
    icon: p.emoji ?? undefined,
    properties: {},
    tags: p.tags,
    favorite: p.is_favorite,
    pinned: p.is_pinned,
    createdAt: p.created_at,
    updatedAt: p.updated_at,
  };
}

function mapFolderToNoteBook(f: Folder): NoteBook {
  return {
    id: f.id,
    title: f.name,
    icon: f.emoji ?? undefined,
    pageIds: [],
    createdAt: f.created_at,
    updatedAt: f.created_at,
    autoGenerated: false,
    chapters: [],
    parentId: f.parent_folder_id,
  };
}

