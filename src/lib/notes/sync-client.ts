// ── Block loading / body saving ────────────────────────────────────

function detectBlockType(
  content: string,
): { type: BlockType; content: string; properties: Record<string, string> } {
  const trimmed = content.trim();
  if (trimmed.startsWith('### '))
    return { type: 'heading', content: trimmed.slice(4), properties: { level: '3' } };
  if (trimmed.startsWith('## '))
    return { type: 'heading', content: trimmed.slice(3), properties: { level: '2' } };
  if (trimmed.startsWith('# '))
    return { type: 'heading', content: trimmed.slice(2), properties: { level: '1' } };
  if (trimmed.startsWith('> '))
    return { type: 'quote', content: trimmed.slice(2), properties: {} };
  if (/^- \[[ x]\] /.test(trimmed)) {
    const checked = trimmed.startsWith('- [x] ');
    return { type: 'todo', content: trimmed.slice(6), properties: { checked: String(checked) } };
  }
  if (trimmed.startsWith('- ') || trimmed.startsWith('* '))
    return { type: 'list-item', content: trimmed.slice(2), properties: {} };
  if (/^\d+\.\s/.test(trimmed))
    return { type: 'numbered-item', content: trimmed.replace(/^\d+\.\s/, ''), properties: {} };
  if (trimmed === '---' || trimmed === '***')
    return { type: 'divider', content: '', properties: {} };
  return { type: 'paragraph', content: trimmed, properties: {} };
}

function mapRustBlockToNoteBlock(b: Block): NoteBlock {
  const detected = detectBlockType(b.content);
  return {
    id: b.id,
    pageId: b.page_id,
    parentId: b.parent_block_id,
    order: String(b.order).padStart(4, '0'),
    indent: b.depth,
    content: detected.content,
    type: detected.type,
    collapsed: b.is_collapsed,
    refs: extractPageLinks(b.content),
    properties: detected.properties,
    createdAt: b.created_at,
    updatedAt: b.updated_at,
  };
}

export async function loadPageBlocks(pageId: string): Promise<NoteBlock[]> {
  // Try structured blocks first (Rust BlockParser output)
  const blocksRes = await commands.getBlocks(pageId);
  if (blocksRes.status === 'ok' && blocksRes.data.length > 0) {
    return blocksRes.data.map(mapRustBlockToNoteBlock);
  }

  // Fallback: load raw markdown body and split into blocks
  const bodyRes = await commands.loadBody(pageId);
  if (bodyRes.status === 'ok' && bodyRes.data.trim()) {
    const lines = bodyRes.data.split('\n');
    const blocks: NoteBlock[] = [];
    let orderIdx = 0;
    for (const line of lines) {
      if (line.trim() === '') continue;
      const detected = detectBlockType(line);
      blocks.push({
        id: generateBlockId(),
        pageId,
        parentId: null,
        order: String(orderIdx).padStart(4, '0'),
        indent: 0,
        content: detected.content,
        type: detected.type,
        collapsed: false,
        refs: extractPageLinks(line),
        properties: detected.properties,
        createdAt: Date.now(),
        updatedAt: Date.now(),
      });
      orderIdx++;
    }
    return blocks;
  }

  return [];
}

function serializeBlockToMarkdown(block: NoteBlock): string {
  const content = stripHtml(block.content);
  switch (block.type) {
    case 'heading': {
      const level = parseInt(block.properties.level || '1', 10);
      return `${'#'.repeat(level)} ${content}`;
    }
    case 'quote': return `> ${content}`;
    case 'list-item': return `- ${content}`;
    case 'numbered-item': return `1. ${content}`;
    case 'todo': return `- [${block.properties.checked === 'true' ? 'x' : ' '}] ${content}`;
    case 'code': return `\`\`\`\n${content}\n\`\`\``;
    case 'divider': return '---';
    default: return content;
  }
}

export async function savePageBodyToBackend(
  pageId: string,
  blocks: NoteBlock[],
): Promise<void> {
  const pageBlocks = blocks
    .filter((b) => b.pageId === pageId)
    .sort((a, b) => a.order.localeCompare(b.order));
  const markdown = pageBlocks.map(serializeBlockToMarkdown).join('\n');
  await commands.saveBody(pageId, markdown);
}
