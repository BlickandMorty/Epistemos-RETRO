export async function updateFolderOnBackend(
  folderId: string,
  update: Partial<Folder>,
): Promise<void> {
  const foldersRes = await commands.listFolders();
  if (foldersRes.status !== 'ok') return;
  const folder = foldersRes.data.find((f) => f.id === folderId);
  if (!folder) return;
  await commands.updateFolder({ ...folder, ...update });
}

